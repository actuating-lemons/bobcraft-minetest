local function register_dye_item(name, index)
	minetest.register_craftitem("bobcraft_dyes:dye_"..name, {
		description = bobutil.titleize(name) .. " Dye",
		inventory_image = "dye_white.png",
		stack_max = bobutil.stack_max,

		color = dyes.ext_colours[index],

		groups = {ext_dye = 1, not_in_creative_inventory=1}
	})

	dyes.ext_items[name] = "bobcraft_dyes:dye_"..name
end

for i = 0, 255 do
	register_dye_item("ext_"..i, i)
end